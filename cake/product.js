const productCon = document.querySelector('.product');
const cateData = document.querySelectorAll('.cate button');
const seData = document.querySelector('#productSearch');
const seBtn = document.querySelector('#searchbtn');

// Function to filter cakes based on selected category
window.addEventListener("DOMContentLoaded",()=>{
  displayCakeData(data); 
});


cateData.forEach((links)=>{
    links.addEventListener("click",(e)=>{
        const category = e.target.dataset.is;
        const cakeCategory = data.filter((data)=>{
            if(data.category === category){
                return data;
            }
        });
        if(category ==="All"){
            displayCakeData(data);
        }else{
            displayCakeData(cakeCategory);
        }
    })
})
function displayCakeData(data){
let displayData = data.map(function(items){
    return `<div class="p1" data-aos="zoom-in">
            <img src="${items.img}" alt="img" width="350px" height="250px">
            <h2>${items.name}</h2>
            <h4>${items.des}</h4>
            <p>Rs. ${items.price}</p>
           <a href="form.html"> <button>${items.btn}</button></a>
        </div>`
});
productCon.innerHTML = displayData;
}

// Search functionality

seBtn.addEventListener("click",(e)=>{
    let searchvalue= seData.value.toLowerCase();
    let searchResult = data.filter((data)=>{
        if(data.name.toLocaleLowerCase().includes(searchvalue)){
            return data;
        }else if(data.category.toLowerCase().includes(searchvalue)){
            return data;
        }
    });
    if(searchResult){
        displayCakeData(searchResult);
    }
    seData.value = "";
});


document.addEventListener('DOMContentLoaded', () => {
    let goToTopBtn = document.getElementById('gotoTop');

    // Show the button when user scrolls down 20px from the top of the document
    window.onscroll = function() {
        scrollFunction();
    };

    function scrollFunction() {
        if (document.body.scrollTop > 20 || document.documentElement.scrollTop > 20) {
            goToTopBtn.style.display = 'block';
        } else {
            goToTopBtn.style.display = 'none';
        }
    }

    // Scroll to the top of the document when the button is clicked
    goToTopBtn.addEventListener('click', () => {
        document.body.scrollTop = 0; // For Safari
        document.documentElement.scrollTop = 0; // For Chrome, Firefox, IE and Opera
    });
});
